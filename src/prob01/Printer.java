package prob01;

public class Printer {
	public void println(int num){
		System.out.println(num);
	}

	public void println(double num){
		System.out.println(num);
	}

	public void println(boolean b){
		System.out.println(b);
	}

	public void println(String s){
		System.out.println(s);
	}
}
